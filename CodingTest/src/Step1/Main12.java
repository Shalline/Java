public class Main12 {

public static void main(String[] args) {
	
	// 첫 번째 줄: \    /\
    System.out.print("\\");  // \
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print("/");   // /
    System.out.print("\\");  // \
    System.out.println();

    // 두 번째 줄:  )  ( ')
    System.out.print(" ");   //  
    System.out.print(")");   // )
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print("(");   // (
    System.out.print(" ");   //  
    System.out.print("'");   // '
    System.out.print(")");   // )
    System.out.println();

    // 세 번째 줄: (  /  )
    System.out.print("(");   // (
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print("/");   // /
    System.out.print(" ");   //  
    System.out.print(" ");   //  
    System.out.print(")");   // )
    System.out.println();

    // 네 번째 줄:  \(__)|
    System.out.print(" ");   //  
    System.out.print("\\");  // \
    System.out.print("(");   // (
    System.out.print("_");   // _
    System.out.print("_");   // _
    System.out.print(")");   // )
    System.out.print("|");   // |
    System.out.println();

	}
}